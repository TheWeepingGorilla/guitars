def main_menu
  loop do
    linespace
    puts "M > Manufacturer Menu"
    puts "B > Body Type Menu"
    puts "G > Guitar Menu"
    puts "X > Exit."
    linespace
    choice = gets.chomp.upcase
    case(choice)
      when 'M'
        brand_menu
      when 'B'
        body_menu
      when 'G'
        guitar_menu
      when 'X'
        puts "Have an excellent day!"
        exit
      else
        puts "Invalid! Try again"
    end
  end
end

